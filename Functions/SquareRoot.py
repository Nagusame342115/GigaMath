def sqrt(x):
    """
    Calculates the square root of a number.

    Parameters:
    - x: the number

    Returns:
    - result: square root of x
    - explanation: step-by-step explanation
    """
    result = x ** 0.5
    explanation = f"Square Root: sqrt({x}) = {result}"
    return result, explanation

