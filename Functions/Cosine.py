def cos(x):
    """
    Calculates the cosine of an angle in radians.

    Parameters:
    - x: angle in radians

    Returns:
    - result: cosine of x
    - explanation: step-by-step explanation
    """
    import math
    result = math.cos(x)
    explanation = f"Cosine: cos({x}) = {result}"
    return result, explanation

