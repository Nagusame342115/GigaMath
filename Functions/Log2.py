def log2(x):
    """
    Calculates the logarithm base 2 of a value.

    Parameters:
    - x: the value

    Returns:
    - result: log base 2 of x
    - explanation: step-by-step explanation
    """
    import math
    result = math.log2(x)
    explanation = f"Logarithm Base 2: log2({x}) = {result}"
    return result, explanation

