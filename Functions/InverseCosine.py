def acos(x):
    """
    Calculates the arccosine of a value.

    Parameters:
    - x: the value

    Returns:
    - result: arccosine of x
    - explanation: step-by-step explanation
    """
    import math
    result = math.acos(x)
    explanation = f"Inverse Cosine: acos({x}) = {result} radians"
    return result, explanation

